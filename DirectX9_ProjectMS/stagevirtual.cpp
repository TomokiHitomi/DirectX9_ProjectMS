//=============================================================================
// コンストラクタ（初期化処理）
//=============================================================================
StageVirtual::StageVirtual(void)
{
	pPlane = NULL;
	pPlane = new Plane;

	pBox = NULL;
	pBox = new Box;
}

//=============================================================================
// デストラクタ（終了処理）
//=============================================================================
StageVirtual::~StageVirtual(void)
{
	SAFE_DELETE(pPlane);
	SAFE_DELETE(pBox);
}

//=============================================================================
// 更新処理
//=============================================================================
void StageVirtual::Update(void)
{
	pPlane->Update();
	pBox->Update();
}

//=============================================================================
// 描画処理
//=============================================================================
void StageVirtual::Draw(void)
{
	pPlane->Draw();
	pBox->Draw();
}